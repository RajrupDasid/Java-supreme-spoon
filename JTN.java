public class JTN{
  // Java narrow casting
// java narrow casting has to done by manually , placing data type in parenthasis
  public static void main(String[] args){
    String inform="We are doing narrow casting here";
    double mydouble = 6;
    int myint  = (int) mydouble;
    System.out.println(inform);
    System.out.println(mydouble);
    System.out.println(myint);
  }
}